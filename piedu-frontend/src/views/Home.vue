    import auth from "@/auth"

    export default {
        name: "home",
        data() {
            return {
                username: String,
            }
        },
        created() {
            this.setUsername();
        },
        watch: {
            $route(to, from) {
                this.setUsername();
            }
        },
        methods: {
            isAuthenticated: function () {
                return auth.isAuthenticated(this);
            },
            logout: function () {
                auth.logout(this);
                this.username = null;
            },
            setUsername: function () {
                this.username = this.$ls.get("username");
            }
        }

    };
</script>
